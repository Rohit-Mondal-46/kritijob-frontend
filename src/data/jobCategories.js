export const JOB_CATEGORY_OPTIONS = JOB_CATEGORIES.map(({ value, label }) => ({ value, label }));

const JOB_CATEGORY_MAP = JOB_CATEGORIES.reduce((accumulator, category) => {
  accumulator[category.value] = category;
  return accumulator;
}, {});

export const getJobCategoryLabel = (value) => JOB_CATEGORY_MAP[value]?.label || value || "";

export const getJobSubcategories = (categoryValue) => JOB_CATEGORY_MAP[categoryValue]?.subcategories || [];

export const isValidJobCategory = (value) => Boolean(JOB_CATEGORY_MAP[value]);

export const isValidJobSubcategory = (categoryValue, subcategoryValue) => {
  if (!categoryValue || !subcategoryValue) return false;
  return getJobSubcategories(categoryValue).includes(subcategoryValue);
};